alert("Área do Trapézio");
let basemaior = parseInt(prompt("Digite o tamanho da base maior: "));
let basemenor = parseInt(prompt("Digite o tamanho da base menor: "));
let altura = parseInt(prompt("Digite o tamanho da altura: "));
let area = (basemaior + basemenor) * altura / 2;
alert("A área do trapézio é: " + area + ".");